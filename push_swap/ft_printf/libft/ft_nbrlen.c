#include "libft.h"

int	ft_nbrlen(long long nbr)
{
	int	i;

	i = 0;
	if (nbr == 0)
		return (1);
	if (nbr < 0)
	{
		nbr = -nbr;
		i++;
	}
	while (nbr)
	{
		nbr = nbr / 10;
		i++;
	}
	return (i);
}
