#include "pushswap.h"

void	ft_free_tab(char **tab, int toggle)
{
	int	i;

	i = 0;
	while (tab[i])
	{
		free(tab[i]);
		i++;
	}
	free(tab);
	if (toggle == 1)
		ft_error();
}
