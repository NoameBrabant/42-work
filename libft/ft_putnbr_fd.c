#include "libft.h"

int	ft_putnbr_fd(t_nb n, int fd)
{
	t_nb	m;

	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return (0);
	}
	if (n < 0)
	{
		write (fd, "-", 1);
		n = -n;
	}
	m = n;
	if (n >= 10)
		ft_putnbr_fd (n / 10, fd);
	m = n % 10 + '0';
	write (fd, &m, 1);
	return (ft_nbrlen(n));
}
